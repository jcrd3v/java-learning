public class DataTypeDemo {
    public static void main(String[] args) {

        char ch = 'A';
        byte by = 127;
        short sh = 32767;
        int i = 1123456;
        long l = 321654879546432l;
        float f = 152.642164f;
        double d = 132654987.6876321678321d;
        boolean b = true;

        System.out.println("This is a char variable: " + ch);
        System.out.println("This is a byte variable: " + by);
        System.out.println("This is a short variable: " + sh);
        System.out.println("This is a int variable: " + i);
        System.out.println("This is a long variable: " + l);
        System.out.println("This is a float variable: " + f);
        System.out.println("This is a double variable: " + d);
        System.out.println("This is a boolean variable: " + b);

    }
}